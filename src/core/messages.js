export function createOk(data = {}) {
  return {
    ok: true,
    data
  };
}

export function createError(code, message) {
  return {
    ok: false,
    error: {
      code: String(code || 'INTERNAL_ERROR'),
      message: String(message || 'Sanitas request failed.')
    }
  };
}
